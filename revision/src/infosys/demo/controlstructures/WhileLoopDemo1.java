package infosys.demo.controlstructures;

public class WhileLoopDemo1 {

	public static void main(String[] args) {
		int i=1; // initialisation step
		// loop with entry point check as the condition to enter the loop is checked before entering the loop
		while(i<=10) { // condition check and the control will enter the loop as the intial value 1 is less than 10
         System.out.println(i);			
		 i++; // Re-initialisation of the counter variable 	 
		}
		

	}

}
