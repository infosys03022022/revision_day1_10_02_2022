package infosys.demo.controlstructures;

public class DoWhileLoopDemo1 {

	public static void main(String[] args) {
		int i=1; // initialisation
		do {
		  System.out.println(i);	
		  i++;   	  // re-initialisation
		}while(i<=10); // condition check
		 // loop with exit point check which means if the condition is false , then also it will execute once	

	}

}
