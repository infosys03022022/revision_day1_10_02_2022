package infosys.demo.variables;

public class Demo1 {

	public static void main(String[] args) {
		  int num;
		  //In line 6, num is a variable and int is a data type
		 num=10;
		//LHS=RHS RHS value is assigned LHS entity which means in
		// above line we are assigning the constant value 10 to the variable num
		 System.out.println(num); // 10
		 num=20; // Intially num was holding the value 10 because of the
		 // statement in line 8 but because of line 12, the value in num, 
		 // is replaced by 20
		System.out.println(num);   // 20
		

	}

}
